package com.esummary.auth.exception;

public class DeniedElearningCookieException extends RuntimeException {
    public DeniedElearningCookieException() {
        super();
    }
    public DeniedElearningCookieException(String message, Throwable cause) {
        super(message, cause);
    }
    public DeniedElearningCookieException(String message) {
        super(message);
    }
    public DeniedElearningCookieException(Throwable cause) {
        super(cause);
    }
}
